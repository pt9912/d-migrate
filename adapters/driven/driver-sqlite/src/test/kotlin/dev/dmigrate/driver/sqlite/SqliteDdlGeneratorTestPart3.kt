    test("multi-column BTREE index generates correct SQL") {
        val s = schema(
            tables = mapOf(
                "events" to table(
                    columns = mapOf(
                        "id" to col(NeutralType.Identifier(autoIncrement = true)),
                        "user_id" to col(NeutralType.Integer, required = true),
                        "created_at" to col(NeutralType.DateTime(), required = true)
                    ),
                    primaryKey = listOf("id"),
                    indices = listOf(
                        IndexDefinition(
                            name = "idx_events_user_date",
                            columns = listOf("user_id", "created_at"),
                            type = IndexType.BTREE
                        )
                    )
                )
            )
        )
        val result = generator.generate(s)
        val indexSql = result.statements
            .map { it.sql.trim() }
            .first { it.startsWith("CREATE INDEX") }

        indexSql shouldBe "CREATE INDEX \"idx_events_user_date\" ON \"events\" (\"user_id\", \"created_at\");"
    }

    test("enum with ref_type and inline reference generates REFERENCES clause") {
        val s = schema(
            customTypes = mapOf(
                "status_type" to CustomTypeDefinition(
                    kind = CustomTypeKind.ENUM,
                    values = listOf("active", "inactive")
                )
            ),
            tables = mapOf(
                "statuses" to table(
                    columns = mapOf(
                        "id" to col(NeutralType.Identifier(autoIncrement = true))
                    ),
                    primaryKey = listOf("id")
                ),
                "accounts" to table(
                    columns = mapOf(
                        "id" to col(NeutralType.Identifier(autoIncrement = true)),
                        "status" to col(
                            NeutralType.Enum(refType = "status_type"),
                            required = true,
                            references = ref("statuses", "id", onDelete = ReferentialAction.RESTRICT)
                        )
                    ),
                    primaryKey = listOf("id")
                )
            )
        )
        val result = generator.generate(s)
        val accountsSql = result.statements
            .map { it.sql.trim() }
            .first { it.contains("\"accounts\"") && it.startsWith("CREATE TABLE") }

        accountsSql shouldContain "CHECK (\"status\" IN ('active', 'inactive'))"
        accountsSql shouldContain "REFERENCES \"statuses\"(\"id\") ON DELETE RESTRICT"
    }

