    test("identifier column without autoIncrement still uses INTEGER PRIMARY KEY AUTOINCREMENT") {
        // NeutralType.Identifier defaults autoIncrement=false, but in the generator
        // the special path only fires when autoIncrement is true.
        val s = schema(
            tables = mapOf(
                "items" to table(
                    columns = mapOf(
                        "id" to col(NeutralType.Identifier(autoIncrement = false)),
                        "name" to col(NeutralType.Text())
                    ),
                    primaryKey = listOf("id")
                )
            )
        )
        val result = generator.generate(s)
        val sql = result.tableSql()

        // With autoIncrement=false, it still maps to INTEGER PRIMARY KEY AUTOINCREMENT via typeMapper
        // but uses the default columnSql path since the special branch checks autoIncrement
        sql shouldContain "\"id\""
    }

    test("multiple indices on same table are all generated") {
        val s = schema(
            tables = mapOf(
